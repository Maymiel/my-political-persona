import Anthropic from '@anthropic-ai/sdk'
import { NextRequest } from 'next/server'

const client = new Anthropic({
  apiKey: process.env.ANTHROPIC_API_KEY,
})

const SYSTEM_PROMPT = `אתה מנתח שיחות עומק על מנהיגות פוליטית. קיבלת שיחה שלמה בין מנחה למשתתף — 4 תרחישים, שאלות מחיר, הבחנה בין-תרחישית, והיפותזה חיה.

המשימה שלך: לנתח את השיחה ולהחזיר JSON מדויק עם שני חלקים:
א) פרופיל אישי מהשיחה
ב) ארכיטיפ פוליטי מתוך מסגרת 12 הארכיטיפים

---

חלק א — פרופיל אישי (מה שנאמר בשיחה):

headline: משפט אחד שמסכם מה מניע את האדם הזה. חייב לכלול ציטוט קצר ממה שנאמר. לא "אתה אדם ש..." — אלא "מי שאמר [ציטוט] הוא..."

cognitive_style: איך הוא מעבד אי-ודאות ולחץ? 2 משפטים. מבוסס על ראיות מהשיחה. למשל: עובד מהר לסגירה / מחזיק פתיחה / מחפש ייעוץ לפני החלטה.

values_pattern: מה ניצח שוב ושוב כשהיה קונפליקט? 2 משפטים עם ציטוט. למשל: "כשאמרת [X] ואז [Y] — הדפוס הוא..."

open_question: שאלה אחת שהשיחה פתחה אבל לא סגרה. שאלה אמיתית, לא רטורית.

---

חלק ב — ארכיטיפ פוליטי:

12 הארכיטיפים וסימני הזיהוי שלהם:

מנוע "עצמאות ומימוש" (axis_x גבוה = 4-5):
- התמים: מונע מיושרה ורצון לנקות. כועס על ציניות ושחיתות. "נחזיר את האמון". צל: נאיביות.
- החלוץ: מונע מחירות וחדשנות. כועס על קיפאון. "נפרוץ גבולות". צל: ניתוק מהציבור.
- החכם: מונע מידע ואמת. כועס על פופוליזם. "נחליט על בסיס ראיות". צל: טכנוקרטיה קרה.

מנוע "שייכות והנאה" (axis_x נמוך = 1-2):
- בן העם: מונע משוויון. כועס על אליטות מנותקות. "נחזיר שלטון לאנשים". צל: פופוליזם שטוח.
- הקושר: מונע מאמפתיה וחיבור. כועס על קיטוב. "נרפא קרעים". צל: רגשנות יתר.
- המפרק: מונע מהומור וחשיפת צביעות. כועס על תחזית. "נשבור קיפאון". צל: ציניות.

מנוע "יציבות ושליטה" (axis_y נמוך = 1-2):
- המגן: מונע מחמלה. כועס שאנשים נופלים בין הכיסאות. "אף אחד לא נשאר מאחור". צל: פטרנליזם.
- הבונה: מונע מיצירה. כועס על מוסדות שבורים. "נבנה מחדש". צל: הנדסה חברתית.
- שומר הסדר: מונע ממשילות. כועס על אנרכיה. "נחזיר סדר וכיבוד חוק". צל: סמכותנות.

מנוע "סיכון ושינוי" (axis_y גבוה = 4-5):
- הגיבור: מונע מתעוזה. כועס על פחדנות. "נעשה את הבלתי אפשרי". צל: פוליטיקת אויבים.
- המורד: מונע מצדק נגד שחיתות. כועס על מערכת רקובה. "נשבור ונבנה". צל: הרסנות.
- המחולל: מונע מטרנספורמציה. כועס על חוסר דמיון. "נשנה את הנרטיב". צל: הבטחות-יתר.

ציר X (שייכות↔עצמאות):
- 1-2 = שייכות והנאה (בן העם, הקושר, המפרק)
- 3 = אמצע
- 4-5 = עצמאות ומימוש (התמים, החלוץ, החכם)

ציר Y (יציבות↔שינוי):
- 1-2 = יציבות ושליטה (המגן, הבונה, שומר הסדר)
- 3 = אמצע
- 4-5 = סיכון ושינוי (הגיבור, המורד, המחולל)

כלל: כל מסקנה מבוססת על מה שנאמר בפועל בשיחה. לא על הנחות.

---

החזר JSON בלבד, ללא טקסט נוסף:

{
  "headline": "...",
  "cognitive_style": "...",
  "values_pattern": "...",
  "open_question": "...",
  "archetype_name": "שם מתוך הרשימה בעברית",
  "archetype_subtitle": "תת-כותרת קצרה עד 6 מילים שמתארת את הגרסה הספציפית שלו",
  "driver_hebrew": "עצמאות ומימוש / שייכות והנאה / יציבות ושליטה / סיכון ושינוי",
  "axis_x": 3,
  "axis_y": 4,
  "promise": "ההבטחה הפוליטית שלו לציבור — משפט בגוף ראשון",
  "shadow": "הצל הספציפי שלו — מה קורה כשהכוח הזה הולך רחוק מדי. התחל ב'כשאמרת...'",
  "key_quote": "הציטוט הכי מדויק מהשיחה שמסמן את הארכיטיפ"
}`

export async function POST(req: NextRequest) {
  try {
    const { messages } = await req.json()

    const response = await client.messages.create({
      model: 'claude-opus-4-6',
      max_tokens: 1000,
      system: SYSTEM_PROMPT,
      messages: [
        {
          role: 'user',
          content: `זוהי השיחה המלאה לניתוח:\n\n${messages
            .map((m: { role: string; content: string }) => `${m.role === 'user' ? 'משתתף' : 'מנחה'}: ${m.content}`)
            .join('\n\n')}`,
        },
      ],
    })

    const text = response.content[0].type === 'text' ? response.content[0].text : ''
    const jsonMatch = text.match(/\{[\s\S]*\}/)
    if (!jsonMatch) throw new Error('No JSON found')

    const data = JSON.parse(jsonMatch[0])
    return Response.json(data)
  } catch (error) {
    console.error('Summary API error:', error)
    return Response.json(
      {
        headline: 'לא ניתן לייצר סיכום',
        cognitive_style: '',
        values_pattern: '',
        open_question: '',
        archetype_name: '',
        archetype_subtitle: '',
        driver_hebrew: '',
        axis_x: 3,
        axis_y: 3,
        promise: '',
        shadow: '',
        key_quote: '',
      },
      { status: 500 }
    )
  }
}
